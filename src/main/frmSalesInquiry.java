    private void FillSummary(){
        Double  GetPayable= 0.0, GetNet= 0.0,GetDiscount= 0.0;
        int rowCount=0,GetPatients=0 ;
        while (rowCount != tblSales.getRowCount()){
                GetPatients = tblSales.getRowCount();
                GetPayable += Double.valueOf(tblSales.getValueAt(rowCount, 8).toString());
                GetDiscount += Double.valueOf(tblSales.getValueAt(rowCount, 7).toString());
                GetNet +=Double.valueOf(tblSales.getValueAt(rowCount, 9).toString());
            rowCount++;
        }
        lblDiscount.setText(String.valueOf(DbConn.df.format(GetDiscount)));
        lblPatients.setText(String.valueOf(GetPatients));
        lblPayable.setText(String.valueOf(DbConn.df.format(GetPayable)));
        lblNet.setText(String.valueOf(DbConn.df.format(GetNet)));
    }
