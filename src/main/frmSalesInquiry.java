    private void FillCurrentDate(){
        try{
            DbConn.SQLQuery ="select ih_number,ih_patientname,ih_apptid,ih_insurance,ih_cash,ih_card,ih_cheque,ih_discount,"
                    + "ih_subtotal,ih_netamount,ih_doctor from tblinvoiceheader where ih_date between ? and ?";
            DbConn.pstmt = DbConn.conn.prepareStatement(DbConn.SQLQuery);
            DbConn.pstmt.setString(1,String.valueOf(DbConn.sdfDate.format(dateFrom.getDate())));
            DbConn.pstmt.setString(2,String.valueOf(DbConn.sdfDate.format(dateTo.getDate())));
            DbConn.rs = DbConn.pstmt.executeQuery();
            tblSales.setModel(DbUtils.resultSetToTableModel(DbConn.rs));
            DbConn.pstmt.close(); 
            FillSummary();
            SetHeader();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
